fun main() {
    val x = 5
    val y = 10
    val z = 5

    if (x == y) {
        println("x is equal to y")
    } else {
        println("x is not equal to y")
    }

    if (x == z) {
        println("x is equal to z")
    } else {
        println("x is not equal to z")
    }

    if (x != y) {
        println("x is not equal to y")
    } else {
        println("x is equal to y")
    }
}
